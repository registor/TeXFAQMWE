#include <stdio.h>
#include <stdlib.h>

#include "bmp.h"
#include "run.h"

void help();

int main(int argc, char *argv[])
{
    help();

    const char* configName = argc == 2 ? argv[1] : "default.ini";

    run(configName);

    return 0;
}

