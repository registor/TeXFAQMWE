void help()
{
    printf(
        "这是一个简单的图像处理程序（PS^-^）\n"
        "它读入一个.INI配置文件并根据配置文件中指定的方式进行处理\n"
        "若未指定配置文件，则载入default.ini配置文件进行处理\n"
        "使用方式:\n"
        "\tbmp config.ini\n\n");
}

